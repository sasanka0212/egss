          <td width="696" bgcolor="#99FF99"><h1 align="justify">&nbsp;&nbsp;<span class="style1">Introduction</span></h1>
            <p align="justify"><strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Polytechnic Education came under National Framework of Technical Education soon  after independence to bridge the gap between Engineers and Workmen.  Towards industrialisation, plans were made to build PSUs, increase the  network of railways and capacity building in Ordnance Factories and  other industries. There was a felt need to develop a pool of technicians who could supervise the work in the workshop and in the site. New  polytechnics were set-up in different plan periods. The polytechnics  initially offered courses in Civil, Mechanical and Electrical  Engineering. With rapid industrialisation processes there was a felt  need for introduction of courses like Mining Engineering, Radio  Communication Engineering, Architecture and the like.</strong></p>
            <p align="justify"><strong>National Policy of Education (NPE), 1986  highlighted the need for strengthening the Polytechnic Education System  to cater to the need of emerging industries and technologies. To develop the Polytechnic Education System to the desired level, it required lot  of investments. Government of India drew a Project for upgradation and  strengthening of the Polytechnic Education in the country with  assistance of the World Bank. The State of West Bengal participated in  the Technician Education-II Project supported by the World Bank (IDA  Credit No. 2223-IN). The project in our State started in 1992 and  completed in October 1999. A total investment of about Rs.142 crores was made under the project. New Polytechnics were set-up, new courses were  introduced and extensive staff development activities were taken-up.  Curricula were updated and revised. Facilities for technician education  were improved for the girls. Workshops and Laboratories were upgraded  and modernised. Civil works was done in all the polytechnics.</strong></p></td>
